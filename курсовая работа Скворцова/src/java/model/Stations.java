package model;

public class Stations {

    private int codeStation;
    private String nameStation;
    private int codeCex;
    private String older;

    public int getCodeStation() {
        return codeStation;
    }

    public void setCodeStation(int codeStation) {
        this.codeStation = codeStation;
    }

    public String getNameStation() {
        return nameStation;
    }

    public void setNameStation(String nameStation) {
        this.nameStation = nameStation;
    }

    public int getCodeCex() {
        return codeCex;
    }

    public void setCodeCex(int codeCex) {
        this.codeCex = codeCex;
    }

    public String getOlder() {
        return older;
    }

    public void setOlder(String older) {
        this.older = older;
    }
}
